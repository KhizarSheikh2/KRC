void showCenteredText(String text, uint8_t textSize = 2, int yOffset = 0) {
  display.clearDisplay();
  display.setTextSize(textSize);
  display.setFont();
  display.setTextColor(SH110X_WHITE);

  int16_t x1, y1;
  uint16_t w, h;
  display.getTextBounds(text.c_str(), 0, 0, &x1, &y1, &w, &h);  // Use c_str() for bounds
  int x = (SCREEN_WIDTH - w) / 2;
  int y = (SCREEN_HEIGHT - h) / 2 + yOffset;

  display.setCursor(x, y);
  display.print(text);  // Print String directly
  display.display();
}

void showStartupScreen() {
  display.clearDisplay();

  // Draw BITA logo centered (32x32 bitmap)
  int logoX = (SCREEN_WIDTH - 32) / 2;  // Center horizontally
  int logoY = (SCREEN_HEIGHT - 32) / 2; // Center vertically
  display.drawBitmap(logoX, logoY, BITA_LOGO, 32, 32, SH110X_WHITE);

  display.display();
  delay(1500);  // Show for 1.5 seconds
  display.clearDisplay();
}

void updateDisplay() {
  display.clearDisplay();
  display.setTextColor(SH110X_WHITE);

  // ===== HEADER =====
  String modeName;
  switch (current_mode) {
    case Temp_sc:   modeName = "TEMP"; break;
    case Season_sc: modeName = "SEASON";      break;
    case CFM_sc:    modeName = "CFM";         break;
  }

  // If setpoint was recently adjusted, override header to show SETPOINT
  if (setpoint_flag && millis() - setpointStartTime < setpointDuration) {
    modeName = "SETPOINT";
  }

  display.setTextSize(2);  // smaller size so long words fit
  int16_t x1, y1;
  uint16_t w, h;
  display.getTextBounds(modeName.c_str(), 0, 0, &x1, &y1, &w, &h);
  int x = (SCREEN_WIDTH - w) / 2;
  display.setCursor(x, 4);
  display.print(modeName);
  display.drawFastHLine(2, 20, SCREEN_WIDTH - 4, SH110X_WHITE);
  
  // ===== MAIN CONTENT =====
  String mainValue;

  if (current_mode == Temp_sc) {
    // Show setpoint value briefly when keys 3/4 pressed, else show temperature
    if (setpoint_flag && millis() - setpointStartTime < setpointDuration) {
      mainValue = String((float)setpointt, 1) + " " + (char)247 + "C";
    } else {
      setpoint_flag = false;  // timer expired, revert to temp
      if (temperature < -100) {
        mainValue = "Error";
      } else {
        mainValue = String(temperature, 1) + " " + (char)247 + "C";
      }
    }
  }else if (current_mode == Season_sc) {
    // mainValue = (seasonsw == 0) ? "SUMMER" : "WINTER";
    mainValue = seasonsw ? "WINTER" : "SUMMER";
  }else if (current_mode == CFM_sc) {
    mainValue = String(end_value) + "%";   // always shows true %, e.g. "60%"
  }

  display.setTextSize(3);
  display.getTextBounds(mainValue.c_str(), 0, 0, &x1, &y1, &w, &h);
  int tempX = (SCREEN_WIDTH - w) / 2;
  int tempY = (SCREEN_HEIGHT - h) / 2 + 12;
  display.setCursor(tempX, tempY);
  display.print(mainValue);
  display.display();
}

